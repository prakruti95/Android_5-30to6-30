package tops20

fun main()
{
    var marks = 56

    if(marks>=70)
    {
        print("A Grade")
    }
    else if(marks>=60)
    {
        print("B Grade")
    }
    else if(marks>=50)
    {
        print("C Grade")
    }
    else if(marks>=40)
    {
        print("D Grade")
    }
    else
    {
        print("fail")
    }

}